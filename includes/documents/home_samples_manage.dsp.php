      <p><strong><strong>Why not use albums or folders?</strong><br>
      </strong>Tags are more flexible and are actually easer to use than folders and albums. When you tag photos you can create a relationship between various photos. This is something that folders and albums simply can't do. </p>
    <p><em>For Example</em>: You just got back from the zoo, and you went a little crazy taking 500 photos. You would like be able pull up just the photos individual animal shots. With folders you would have to create a seperate folders for each animal and manully seperate or copy the photos over to each. With tags you can assign multiple keywords that would allow you to seperate photos based on subject matter. (the lion photo might have <em>zoo, lion, sleeping, hungry, roar</em>)The tagging possibilities are endless and are easy to add or take away with out actually moving a single photo. If you did a search on &quot;sleeping&quot;, you would get the lion photos and other photos tagged with sleeping. If you did a search on &quot;<em>lion, sleeeping</em>&quot; you would only get the lion photos. <br>
      <br>
