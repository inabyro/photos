    <td width="650">Located at the bottom of the object's properties, there are two small arrows on a timeline. By default when object(quote box, text, etc) has been added to a photo the object is set to appear when the photo is loaded and stay visible for the entire length of the frame. The arrow on the left represents when the object will appear up on the photo during playback. The arrow on the right represents when the object will disappear from the photo. While you are editing an object in a slideshow, drag the &quot;in&quot; arrow (the arrow on the left) and &quot;out&quot; arrow to the desired location (see below in the video). For instance if the delay on the photos is 4 seconds and you move the object's &quot;in&quot; arrow to the half way point, the object will appear after two seconds. </td>
