      <p align="center" class="style2"><span class="style8">Press Release – For Immediate Release
  

  Photagious: Your online photo addiction starts here </span><br>
          <br>
        <em>Flagship new product from FotoFlix delivers flexibility with one-stop shop and unlimited storage and creativity, redefining personal and professional applications of digital photos on web sites, blogs, slideshows and presentations</em></p>
      <p class="style2 style5"> [COVINGTON, Kentucky ~ April 16, 2007] The addicted have spoken. And, like most people who can’t get enough of a certain something, the addiction is spreading. Welcome to Photagious, your online photography addiction.</p>
      <p class="style6">Three years after the first fans, friends and family started getting hooked on FotoFlix, Inc., their online boutique photo format has been recrafted into Photagious, a highly functional and flexible one-stop shop whose unlimited storage credo is complemented by the boundless potential of its ever-changing and growing applications.</p>
      <p class="style6">Once Photagious is in the blood, the addiction can become all-consuming, pervading into both personal and professional affairs.</p>
      <p class="style6">The new Photagious (www.Photagious.com) does everything the old FotoFlix used to do, in addition to being reincarnated as a more robust, savvy online imaging partner enhanced by music, titles, colors, blogs, laughter and life. Once hooked on Photagious, users share, archive, manage and build virtual communities around their photos and<a href="http://www.photagious.com/?action=home.samples"> slideshows</a>, using the fastest and most efficient software and technology available to allow customers to either use the company’s vast library of themes or create their own.</p>
      <p class="style6">“What we provide is a level of sophistication and differentiation, by presenting superior options for presentation,” said Mr. Jon Vreeland, the founder and owner of Photagious, a veteran web designer and programmer who built the online presence of media giant Clear Channel Communications. “Photagious operates in real time. Users are embedding Photagious on web sites, blogs, virtually anywhere – and some people are actually posting on multiple blogs – combining a multimedia option of still photos, video and <a href="http://www.photagious.com/?action=home.samples">slideshows</a>.</p>
      <p class="style6">“Photagious allows you to pull all of that together, in a highly flexible and real-time format. If you are an ad agency, for example, and someone wants to update a partner website or make 20 changes to a presentation, it is updated that second,” Vreeland added.</p>
      <p class="style6"> The company enjoys an established niche in the dynamic and competitive vertical market of digital photo management, employs a half-dozen full- and part-time employees and recently relocated to a Madison Avenue office in the heart of downtown Covington, down the street from the Northern Kentucky E-Zone business accelerator that gave it life.</p>
      <p class="style6">Today’s Photagious works more intuitively, inspires more creativity, and has carved its way into more applications by virtue of the experience had by Photagious fans themselves. Its new paths, features, bells and whistles were built based on feedback from FotoFlix’ growing membership of more than 8,000.</p>
      <p class="style6">What they have told Vreeland and Jaisen Mathai, the Chief Technology Officer at Photagious, is their digital photo service must be flexible, customizable, easy-to-use and virtually limitless in terms of potential. They want a wide palette of controls and options, with the ability to instantly share their inspiration in multiple formats such as on web sites, on blogs, in business presentations and personal <a href="http://www.photagious.com/?action=home.samples">slideshows</a>. It must be, simultaneously, the most technologically advanced and user-friendly digital photo option on the web.</p>
      <p class="style6"> The technology supporting Photagious is complicated, but from a layman’s perspective, “It’s the simplest web service you have ever seen,” said Mr. Mathai. “We utilize a highly efficient development infrastructure using PHP, JSON and AJAX. Let’s put it this way: With a little imagination and a few lines of code, the technology only becomes easier to build valuable applications.”</p>
      <p class="style6">JSON is supported by the largest tech companies in the world, Mathai said. The text format is built on two structures, enumerated arrays and associative arrays, which are similar to the XML language, with several distinct advantages. </p>
      <p class="style6">“We have been fortunate enough to figure out how to reduce development time, due to the simplicity, and the beauty, of JSON,” Mathai added.</p>
      <p class="style6">All technical jargon aside, what convinced Vreeland and his team that they were onto something big with the new Photagious was that ultimate of consumer litmus tests: the in-laws. </p>
      <p class="style6">“One of the things that really sold our investment team was one gentleman’s wife was able to make a <a href="http://www.photagious.com/?action=home.samples">slideshow</a> the first time she went to the site,” Vreeland said. “So he told his mother-in-law, and she was able to use it, no problem. That’s when he was convinced we knew what we were doing.”</p>
      <p align="center" class="style2 style3"><strong>Addiction to Photagious growing deeper </strong></p>
      <p class="style6">“Day by day, the addiction to Photagious grows deeper. Photagious is where your online photo addiction starts,” continued Mr. Vreeland. “The changes that we have made are all about putting everything at peoples’ fingertips. That’s the theory behind making the system easier for people to be creative in telling their story.”</p>
      <p class="style6">Mr. Vreeland started FotoFlix in 2003, officially launching in 2004 with his partner, Mathai. The pair met at Clear Channel, where they specialized in design and technology, respectively, in the telecom giant’s Internet division. Their team includes Kevin Hornschmeier, lead developer, and Rachel Mertens, office manager. The company would like to grow to up to 20 employees, while keeping its lean and mean constitution. <br>
          <br>
        Fotoflix was renowned throughout the blogosphere for its user- and blogger-friendly architecture and approach, which featured a “blogger’s blog.” Like FotoFlix, Photagious relies on security techniques such as pornography and nudity-detecting software. But its true value is found in terms of versatility and flexibility.</p>
      <p class="style5"> <span class="style2">“With the new version, you can add a <a href="http://www.photagious.com/?action=home.samples">slideshow</a> onto your blog, and edit that slideshow directly from your own site. There is real-time editing hotspots, graphics and text that can be manipulated over any photo or video,” Mr. Vreeland said. “It’s also very advantageous to a business or someone who’s into blogging, wants to add content, and doesn’t want to jump to multiple sites.</span></p>
      <p class="style6">“We offer more than 35 different themes. If a user wants to match their' own site or blog, they can get all the way down to defining text colors and background graphics,” Mr. Vreeland concluded.
                                      
Photagious charges $3.95 per month, or $39.95 annually. A Professional upgrade costs $9.95 monthly, offering communications to industries such as broadcast, auctions, classifieds, dating and real estate. A seven-day guest membership is available free of charge. For your addiction, visit <a href="http://www.Photagious.com">www.Photagious.com</a>.<br>
